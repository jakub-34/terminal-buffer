package com.terminalbuffer;

/**
 * Represents terminal foreground/background colors.
 * Includes the DEFAULT (terminal's own default) plus 16 standard ANSI terminal colors
 * (8 normal + 8 bright variants).
 */
public enum Color {
    DEFAULT,
    BLACK,
    RED,
    GREEN,
    YELLOW,
    BLUE,
    MAGENTA,
    CYAN,
    WHITE,
    BRIGHT_BLACK,
    BRIGHT_RED,
    BRIGHT_GREEN,
    BRIGHT_YELLOW,
    BRIGHT_BLUE,
    BRIGHT_MAGENTA,
    BRIGHT_CYAN,
    BRIGHT_WHITE
}

